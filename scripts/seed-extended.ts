const db = new Database(DB_PATH);
db.pragma('foreign_keys = ON');

const insertStmt = db.prepare(`
  INSERT OR IGNORE INTO questions
    (subject, theory_type, category_type, question_type, content, options, options_zhuyin, correct_answer, explanation, score)
  VALUES (@subject, @theory_type, @category_type, @question_type, @content, @options, @options_zhuyin, @correct_answer, @explanation, @score)
`);

let inserted = 0;
let skipped = 0;

const insertMany = db.transaction((qs: Q[]) => {
  for (const q of qs) {
    const { options: shuffledOptions, answer: shuffledAnswer } =
      shuffleSingleChoice(q.options, q.answer);
    const row = {
      subject: q.subject ?? 'chinese',
      theory_type: q.theory_type,
      category_type: q.category_type,
      question_type: q.question_type,
      content: JSON.stringify(zhuyinize(q.prompt)),
      options: JSON.stringify(shuffledOptions),
      options_zhuyin: JSON.stringify(optionsZhuyin(shuffledOptions)),
      correct_answer: shuffledAnswer,
      explanation: q.explanation,
      score: 10,
    };
    const r = insertStmt.run(row);
    if (r.changes > 0) inserted++; else skipped++;
  }
});
insertMany(all);

const totalAfter = (db.prepare('SELECT COUNT(*) c FROM questions').get() as { c: number }).c;
console.log(`[seed-extended] ✅ 新增 ${inserted} 題，略過 ${skipped} 題（已存在）。DB 目前共 ${totalAfter} 題。`);
db.close();
