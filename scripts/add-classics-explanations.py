def patch(path: Path, table: dict[str, str], label: str) -> None:
    if not path.exists():
        print(f'[X] {path} not found', file=sys.stderr)
        return
    data = json.loads(path.read_text(encoding='utf-8'))
    matched = 0
    for entry in data:
        pc = entry.get('pairContext', '')
        if pc in table:
            entry['explanation'] = table[pc]
            matched += 1
    path.write_text(json.dumps(data, ensure_ascii=False, separators=(',', ':')), encoding='utf-8')
    print(f'[{label}] {matched}/{len(data)} 題已加上連句解釋')


def main() -> None:
    patch(SZJ_PATH, SZJ_EXPLANATIONS, '三字經')
    patch(DZG_PATH, DZG_EXPLANATIONS, '弟子規')
    print('done.')


if __name__ == '__main__':
    main()
